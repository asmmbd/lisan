export function getNestedMessage(language: Language, key: string): string {
  const parts = key.split('.')
  let current: string | Messages | undefined = messages[language]

  for (const part of parts) {
    if (!current || typeof current === 'string') break
    current = current[part]
  }

  return typeof current === 'string' ? current : key
}

export function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template

  return Object.entries(vars).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value))
  }, template)
}

export function formatNumber(value: number, language: Language) {
  const locale = language === 'ar' ? 'ar' : 'bn-BD'
  return new Intl.NumberFormat(locale).format(value)
}

export function getLanguageDirection(language: Language) {
  return language === 'ar' ? 'rtl' : 'ltr'
}

export function getLocalizedTextClass(language: Language) {
  return language === 'ar' ? 'arabic-text' : 'bengali-text'
}
